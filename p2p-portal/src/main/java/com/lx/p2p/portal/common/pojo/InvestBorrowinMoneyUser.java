package com.lx.p2p.portal.common.pojo;

public class InvestBorrowinMoneyUser {

	private String userName;
	
	private String guarantee;
	
	private String borrowingAmount;
	
	private Double interestRate;
	
	private String loanTerm;
	
	private String repaymentMode;
	
	private Double investorMoney;

	private Double investSum;
	
	private Double interestSum;
	
	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getGuarantee() {
		return guarantee;
	}

	public void setGuarantee(String guarantee) {
		this.guarantee = guarantee;
	}

	public String getBorrowingAmount() {
		return borrowingAmount;
	}

	public void setBorrowingAmount(String borrowingAmount) {
		this.borrowingAmount = borrowingAmount;
	}

	public Double getInterestRate() {
		return interestRate;
	}

	public void setInterestRate(Double interestRate) {
		this.interestRate = interestRate;
	}

	public String getLoanTerm() {
		return loanTerm;
	}

	public void setLoanTerm(String loanTerm) {
		this.loanTerm = loanTerm;
	}

	public String getRepaymentMode() {
		return repaymentMode;
	}

	public void setRepaymentMode(String repaymentMode) {
		this.repaymentMode = repaymentMode;
	}

	public Double getInvestorMoney() {
		return investorMoney;
	}

	public void setInvestorMoney(Double investorMoney) {
		this.investorMoney = investorMoney;
	}

	public Double getInvestSum() {
		return investSum;
	}

	public void setInvestSum(Double investSum) {
		this.investSum = investSum;
	}

	public Double getInterestSum() {
		return interestSum;
	}

	public void setInterestSum(Double interestSum) {
		this.interestSum = interestSum;
	}
	
	
}
