package com.lx.p2p.portal.entity;

public class PlatformData {
    private Integer platformId;

    private Double platformMoney;

    private Double platformInterested;

    private Integer platformCountPeople;

    private Double platformBalance;

    public Integer getPlatformId() {
        return platformId;
    }

    public void setPlatformId(Integer platformId) {
        this.platformId = platformId;
    }

    public Double getPlatformMoney() {
        return platformMoney;
    }

    public void setPlatformMoney(Double platformMoney) {
        this.platformMoney = platformMoney;
    }

    public Double getPlatformInterested() {
        return platformInterested;
    }

    public void setPlatformInterested(Double platformInterested) {
        this.platformInterested = platformInterested;
    }

    public Integer getPlatformCountPeople() {
        return platformCountPeople;
    }

    public void setPlatformCountPeople(Integer platformCountPeople) {
        this.platformCountPeople = platformCountPeople;
    }

    public Double getPlatformBalance() {
        return platformBalance;
    }

    public void setPlatformBalance(Double platformBalance) {
        this.platformBalance = platformBalance;
    }
}